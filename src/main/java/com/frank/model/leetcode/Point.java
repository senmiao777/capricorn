package com.frank.model.leetcode;

/**
 * 坐标类
 * @author frank
 * @version 1.0
 * @date 2021/5/22 0022 下午 9:49
 */
public class Point {
    private int x;
    private int y;

    public Point(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }
}
